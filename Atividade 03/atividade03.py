"""
Crie um programa que leia dois números e mostre a soma entre eles.
"""
# Aqui pego as informações do usuário que ele deseja somar:
num1 = int(input("Digite um número: "))
num2 = int(input("Digite outro número: "))
# aqui irei fazer o sistema par somar os números que ele informou:
soma = (num1 + num2)
# aqui irei mostra para o usuário o resultado que o sistema resolveu
print(f"A soma dos dois número é: {soma}")